print("\n***** Operators *****\n")

print(f"\n2.23  == 2.23        => \t{2.23 == 2.23}")
print(f"\n2     == 2.23        => \t{2 == 2.23}")
print(f"\n'car' == 'car'       => \t{'car' == 'car'}")
print(f"\n'car' != 'car'       => \t{'car' != 'car'}")

print(f"\nTrue and False       => \t{True and False}")
print(f"\nTrue or False        => \t{True or False}")

print(f"\n3 in [1, 2, 3]       => \t{3 in [1, 2, 3]}")
print(f"\n3 not in [1, 2, 3]   => \t{3 not in [1, 2, 3]}")

print(f"\n10 % 3               => \t{10%3}")